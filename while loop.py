# =========================================================Python While Loops========================================


# The while Loop

i=1
while i < 7:
    print(i)
    i += 1
#
# # The break Statement
#
i=1
while i < 7:
    print(i)
    if i == 5:
        break
    i += 1
# #
# # The continue Statement

i = 1
while i < 7:
    i += 1
    if i == 5:
        continue
    print(i)


# else statement
i = 1
while i < 8:
   print(i)
   i += 1
else:
   print("the end")



