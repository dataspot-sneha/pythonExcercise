# Python Lambda


# Lambda Functions
# A lambda function is a small anonymous function.
# A lambda function can take any number of arguments, but can only have one expression.


# --------------------------------------------------------------------------------------------
# Syntax
# lambda arguments : expression
# ----------------------------------------------------------------------------------------------




# example1
# Add 100 to argument y, and return the result:

x = lambda y : y + 100
print(x(5))
# here  value of y is 5 and value of x  100 as stored in variable


# example2
# Multiply argument p with argument q and return the result:

x = lambda p, q : p * q
print(x(59, 61))


# Example3
# Summarize argument a, b, and c and return the result:

x = lambda a, b, c : a + b + c
print(x(52, 16, 12))
